
angka1 = int(input("Masukkan Angka Pertama  : "))
angka2 = int(input("Masukkan Angka Kedua    : "))
    
add = angka1 + angka2
sub = angka1 - angka2
mul = angka1 * angka2
div = angka1 / angka2
    
print("Hasil Penjumlahan       : ", add)
print("Hasil Pengurangan       : ", sub)
print("Hasil Perkalian         : ", mul)
print("Hasil Pembagian         : ", div)